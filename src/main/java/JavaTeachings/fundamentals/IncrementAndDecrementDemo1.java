package JavaTeachings.fundamentals;

public class IncrementAndDecrementDemo1 {
    public static void main(String[] args) {
        int x=10;
        x++;
        System.out.println("Value of x is :"+x);
        ++x;
        System.out.println("Value of x is :"+x);
        --x;
        System.out.println("Value of x is :"+x);
        x--;
        System.out.println("Value of x is :"+x);

    }
}
