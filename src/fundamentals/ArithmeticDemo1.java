package fundamentals;

public class ArithmeticDemo1 {
    public static void main(String[] args) {
        int i='c';
        int j=i+10;
        System.out.println("value of j is :"+j);

        j=i-10;
        System.out.println("Value of j is "+j);

        j=j*10;
        System.out.println("Value of j is :"+j);

        int x=43;
        int y=x/7;
        System.out.println("Value of y is "+y);

        int z=x%7;
        System.out.println("Value of z is :"+z);
    }
}
